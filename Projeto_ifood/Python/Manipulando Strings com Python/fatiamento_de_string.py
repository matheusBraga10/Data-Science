nome = 'Matheus Felipe Braga de Souza'

print(nome[0]) # Exibe a letra da casa citada
print(nome[:9]) # Exibe até a casa da letra citada
print(nome[10:]) # Exibe a letra desde a casa citada
print(nome[10:16]) # Exibe as letras entre as casas citadas
print(nome[10:16:2]) # Exibe as letras entre as casas citadas, pulando de 2 em 2 passo citado
print(nome[:]) # Exibe a string inteira
print(nome[::-1]) # Exibe de trás para frente
